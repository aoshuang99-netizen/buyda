/**
 * 搜索知识库
 */
export function searchKnowledgeBase(query: string, maxResults = 5): KnowledgeItem[] {
  const lowerQuery = query.toLowerCase();
  
  // 计算相关性得分
  const results = WHITE_PAPER_KNOWLEDGE_BASE.map(item => {
    let score = 0;
    
    // 问题匹配（最高权重）
    if (item.question.toLowerCase().includes(lowerQuery)) {
      score += 10;
    }
    
    // 关键词匹配
    item.keywords.forEach(keyword => {
      if (keyword.toLowerCase().includes(lowerQuery)) {
        score += 5;
      }
    });
    
    // 答案匹配
    if (item.answer.toLowerCase().includes(lowerQuery)) {
      score += 2;
    }
    
    // 分类匹配
    if (item.category.toLowerCase().includes(lowerQuery)) {
      score += 3;
    }
    
    return { item, score };
  });
  
  // 按得分排序，取前N个
  return results
    .filter(r => r.score > 0)
    .sort((a, b) => b.score - a.score)
    .slice(0, maxResults)
    .map(r => r.item);
}

/**
 * 获取所有分类
 */
export function getAllCategories(): string[] {
  const categories = new Set(WHITE_PAPER_KNOWLEDGE_BASE.map(item => item.category));
  return Array.from(categories).sort();
}

/**
 * 按分类获取知识条目
 */
export function getKnowledgeByCategory(category: string): KnowledgeItem[] {
  return WHITE_PAPER_KNOWLEDGE_BASE.filter(item => item.category === category);
}
